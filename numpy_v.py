import numpy as np

# Creating a 1D array
arr1 = np.array([1, 2, 3, 4, 5])
print("1D array:", arr1)

# Creating a 2D array filled with zeros
arr2 = np.zeros((3, 3))
print("2D array filled with zeros:", arr2)

# Creating a 3D array filled with ones
arr3 = np.ones((2, 2, 2))
print("3D array filled with ones:", arr3)